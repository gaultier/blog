fn generate_all(cache: &mut HashMap<String, Article>) {
    let start = std::time::Instant::now();
    let html_header = fs::read("header.html").unwrap();
    let html_footer = fs::read("footer.html").unwrap();

    let git_stats = git_get_articles_stats();
    let mut search_index = SearchIndex::new();
    let mut articles: Vec<Article> = git_stats
        .into_iter()
        .filter(|gs| {
            gs.path_from_git_root != "README.md"
                && gs.path_from_git_root != "todo.md"
                && gs.path_from_git_root != "index.md"
        })
        .map(|gs| md_render_article(gs, &html_header, &html_footer, cache, &mut search_index))
        .collect();

    {
        let start = std::time::Instant::now();
        let search_index_file = File::create("search_index.postcard").unwrap();
        // Sort to avoid spurious diffs (and cache busting in the browser).
        // FIXME: This does not work yet.
        search_index
            .trigram_to_file_idx
            .values_mut()
            .for_each(|v| v.sort_by(|a, b| a.0.cmp(&b.0)));
        search_index.files.sort_by(|a, b| a.cmp(&b));

        postcard::to_io(&search_index, search_index_file).unwrap();
        println!(
            "🔍 marshalled search index (count:{}) in {} ms",
            search_index.trigram_to_file_idx.len(),
            Instant::now().duration_since(start).as_millis()
        );
    }
    generate_home_page(&mut articles, &html_header, &html_footer);
    generate_tags_page(&articles, &html_header, &html_footer);
    generate_rss(&mut articles);

    println!(
        "⚙️ generated {} articles in {} ms",
        articles.len(),
        Instant::now().duration_since(start).as_millis()
    );
}

