type Trigram = String;

type FileIdx = u16;

#[derive(Debug, Serialize, Deserialize)]
struct SearchIndex {
    trigram_to_file_idx: BTreeMap<Trigram, Vec<(FileIdx, u32)>>,
    files: Vec<String>,
}

impl SearchIndex {
    fn new() -> Self {
        Self {
            trigram_to_file_idx: BTreeMap::new(),
            files: Vec::with_capacity(128),
        }
    }

    fn ingest_md_ast(&mut self, md_ast: &Node, html_path: &Path) {
        self.files.push(html_path.to_str().unwrap().to_string());
        self.ingest_md_ast_rec(md_ast, FileIdx::try_from(self.files.len() - 1).unwrap());
    }

    fn ingest_md_ast_rec(&mut self, node: &Node, file_idx: FileIdx) {
        match node {
            Node::Root(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::Blockquote(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::FootnoteDefinition(_) => {}
            Node::MdxJsxFlowElement(_) => {}
            Node::List(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::MdxjsEsm(_) => {}
            Node::Toml(_) => {}
            Node::Yaml(_) => {}
            Node::Break(_) => {}
            Node::InlineCode(_) => {}
            Node::InlineMath(_inline_math) => todo!(),
            Node::Delete(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::Emphasis(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::MdxTextExpression(_) => {}
            Node::FootnoteReference(_) => {}
            Node::Html(_) => {}
            Node::Image(_) => {}
            Node::ImageReference(_) => {}
            Node::MdxJsxTextElement(_) => {}
            Node::Link(_) => {}
            Node::LinkReference(_) => {}
            Node::Strong(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::Text(text) => {
                self.ingest_text(&text.value, file_idx);
            }
            Node::Code(_) => {}
            Node::Math(_) => {}
            Node::MdxFlowExpression(_) => {}
            Node::Heading(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::Table(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::ThematicBreak(_) => {}
            Node::TableRow(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::TableCell(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::ListItem(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
            Node::Definition(_) => {}
            Node::Paragraph(x) => {
                for child in &x.children {
                    self.ingest_md_ast_rec(child, file_idx);
                }
            }
        }
    }

    fn ingest_text(&mut self, text: &str, file_idx: FileIdx) {
        let chars: Vec<char> = text.to_lowercase().chars().collect();

        chars.windows(3).for_each(|w| {
            let trigram: String = w.iter().collect::<String>().trim_ascii().to_string();
            self.trigram_to_file_idx
                .entry(trigram)
                .and_modify(|e| {
                    if let Some(elem) = e.iter_mut().find(|(fi, _)| *fi == file_idx) {
                        elem.1 += 1;
                    } else {
                        e.push((file_idx, 1));
                    }
                })
                .or_default();
        });
    }
}

