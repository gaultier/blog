fn generate_all(cache: &mut HashMap<u64, Article>) -> anyhow::Result<()> {
    let start = std::time::Instant::now();
    let html_header = fs::read("header.html").context("failed to read header.html")?;
    let html_footer = fs::read("footer.html").context("failed to read footer.html")?;

    let git_stats = git_get_articles_stats()?;

    let mut articles: Vec<Article> = Vec::with_capacity(git_stats.len());
    for gs in git_stats {
        if gs.path_from_git_root == "README.md"
            || gs.path_from_git_root == "todo.md"
            || gs.path_from_git_root == "index.md"
        {
            continue;
        }

        match md_render_article(gs, &html_header, &html_footer, cache) {
            Ok(a) => {
                articles.push(a);
            }
            Err(err) => {
                eprintln!("err: failed to render article: err={:?}", err);
            }
        }
    }
    for a in &articles {
        fs::write(&a.html_path, &a.html_output)
            .with_context(|| format!("failed to write {:?}", &a.html_path))?;
    }

    generate_home_page(&mut articles, &html_header, &html_footer)?;
    generate_tags_page(&articles, &html_header, &html_footer)?;
    generate_rss(&mut articles)?;
    let articles_count = articles.len();

    println!(
        "⚙️ generated {} articles in {} ms",
        articles_count,
        Instant::now().duration_since(start).as_millis()
    );
    Ok(())
}

