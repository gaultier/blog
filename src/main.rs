fn hash_article_inputs(html_header: &[u8], html_footer: &[u8], md_content: &[u8]) -> u64 {
    let mut hasher = DefaultHasher::new();
    html_header.hash(&mut hasher);
    html_footer.hash(&mut hasher);
    md_content.hash(&mut hasher);
    hasher.finish()
}

fn md_lint_rec(node: &Node, md_path: &Path) -> anyhow::Result<()> {
    match node {
        Node::Root(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::Blockquote(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::FootnoteDefinition(_) | Node::MdxJsxFlowElement(_) => Ok(()),
        Node::List(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::InlineMath(_)
        | Node::MdxjsEsm(_)
        | Node::Toml(_)
        | Node::Yaml(_)
        | Node::Break(_)
        | Node::InlineCode(_) => Ok(()),
        Node::Delete(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::Emphasis(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::MdxTextExpression(_)
        | Node::FootnoteReference(_)
        | Node::Html(_)
        | Node::Image(_)
        | Node::ImageReference(_)
        | Node::MdxJsxTextElement(_) => Ok(()),

        Node::Link(link) => {
            // TODO: external links.
            if !link.url.starts_with("/") {
                return Ok(());
            }

            if !link.url.starts_with("/blog/") {
                bail!("wrong internal link, must start with '/blog': {}", link.url);
            }
            if link.url.ends_with(".md") {
                bail!("wrong internal link, must not end with '.md': {}", link.url);
            }

            if link.url.contains("gaultier.github.io") {
                bail!("wrong internal link, points to public blog: {}", link.url);
            }
            Ok(())
        }
        Node::LinkReference(_) => Ok(()),
        Node::Strong(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::Text(text) => {
            // Check that `DTrace` has the correct case.
            let incorrect = ["dtrace", "dTrace", "Dtrace", "Kib", "kb", "KB", "Kb"];
            for inc in incorrect {
                if let Some(_pos) = text.value.find(inc) {
                    bail!(
                        "incorrect casing for DTrace: file={:?} position={:?} excerpt:{}",
                        md_path.to_str(),
                        text.position,
                        inc
                    );
                }
            }
            Ok(())
        }
        Node::Code(code) => {
            match &code.lang {
                None => {
                    bail!(
                        "missing language for code block: file={:?} position={:?}",
                        md_path.to_str(),
                        code.position
                    );
                }
                Some(lang) => {
                    if !(STANDARD_LANGS.contains(&lang.as_str())
                        || CUSTOM_LANGS.contains(&lang.as_str()))
                    {
                        bail!("unknown lang: {} position={:?}", lang, code.position);
                    }
                }
            }
            Ok(())
        }
        Node::Math(_) | Node::MdxFlowExpression(_) => Ok(()),
        Node::Heading(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::Table(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::ThematicBreak(_) => Ok(()),
        Node::TableRow(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::TableCell(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::ListItem(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
        Node::Definition(_) => Ok(()),
        Node::Paragraph(x) => {
            for child in &x.children {
                md_lint_rec(child, md_path)?;
            }
            Ok(())
        }
    }
}

fn git_get_articles_stats() -> anyhow::Result<Vec<GitStat>> {
    let start = Instant::now();

    let output = Command::new("git")
        .args([
            "log",
            "--format='%aI'",
            "--no-merges",
            "--diff-filter=AMRD",
            "--name-status",
            "--reverse",
            "*.md",
        ])
        .output()
        .context("failed to get git stats")?;
    assert_eq!(Some(0), output.status.code());
    assert!(output.stderr.is_empty());
    println!(
        "git invocation: {} ms",
        Instant::now().duration_since(start).as_millis()
    );

    // Sample git output:
    // 2024-10-31T16:09:02+01:00
    //
    // M       lessons_learned_from_a_successful_rust_rewrite.md
    // A       tip_of_day_3.md
    // 2025-02-18T08:07:55+01:00
    //
    // R100    sha.md  making_my_debug_build_run_100_times_faster.md

    let output_str = String::from_utf8_lossy(&output.stdout);
    let mut lines = output_str.lines().peekable();

    let mut res: BTreeMap<String, GitStat> = BTreeMap::new();

    loop {
        // End?
        let line = match lines.next() {
            None => {
                break;
            }
            Some(line) => line,
        };

        let date_trimmed = line.trim_matches('\'');
        assert!(!date_trimmed.is_empty());

        let empty = lines
            .next()
            .ok_or(anyhow!("expected empty line in git log entry, after date"))?;
        assert!(empty == "");

        // Files.
        loop {
            // End?
            let line = match lines.peek() {
                None => {
                    break;
                }
                Some(line) if *line == "" || line.starts_with("'20") => break,
                Some(_) => lines.next().unwrap(), // Safe due to `peek()`.
            };

            let mut split = line.splitn(3, '\t');
            match (split.next(), split.next(), split.next()) {
                (Some("D"), Some(path), None) => {
                    assert!(!path.is_empty());
                    res.remove(path).ok_or(anyhow!(
                        "expected path in delete action in git log to already be known: {}",
                        path
                    ))?;
                }
                (Some("A"), Some(path), None) => {
                    assert!(!path.is_empty());
                    let git_stat = GitStat {
                        creation_date: date_trimmed.to_owned(),
                        modification_date: date_trimmed.to_owned(),
                        path_from_git_root: path.to_owned(),
                    };
                    res.insert(path.to_owned(), git_stat);
                }
                (Some("M"), Some(path), None) => {
                    let entry = res.get_mut(path).ok_or(anyhow!(
                        "expected path in modified action in git log to already be known: {}",
                        path
                    ))?;
                    assert_ne!(
                        entry.modification_date.as_str().cmp(date_trimmed),
                        Ordering::Greater
                    );
                    // Update the modification date.
                    entry.modification_date = date_trimmed.to_owned();
                }
                (Some(action), Some(path_old), Some(path_new)) if action.starts_with("R") => {
                    assert!(!path_old.is_empty());
                    assert!(!path_new.is_empty());

                    let git_stat = GitStat {
                        creation_date: date_trimmed.to_owned(),
                        modification_date: date_trimmed.to_owned(),
                        path_from_git_root: path_new.to_owned(),
                    };
                    res.remove(path_old);
                    res.insert(path_new.to_owned(), git_stat);
                }
                _ => {
                    bail!("invalid combination in git log entry: `{}`", line);
                }
            }
        }
    }

    let res: Vec<GitStat> = res.into_values().collect();

    println!(
        "🗄️ git stats: {} files in {} ms",
        res.len(),
        Instant::now().duration_since(start).as_millis()
    );

    Ok(res)
}

// TODO: Return Rc<String> or Cow<str>?
fn md_collect_titles(
    node: &Node,
    title_to_counter: &mut BTreeMap<String, u8>,
    titles: &mut Vec<Title>,
) -> anyhow::Result<()> {
    match node {
        Node::Root(root) => {
            for child in &root.children {
                md_collect_titles(child, title_to_counter, titles)?;
            }
        }
        Node::Heading(heading) => {
            let depth = heading.depth;
            // TODO: Handle markdown title!
            assert_eq!(1, heading.children.len());
            let child = heading
                .children
                .first()
                .ok_or(anyhow!("heading has no children"))?;
            let content = match child {
                Node::Text(Text { value, .. }) => value.clone(),
                other => panic!("unexpected value: {:#?}", other),
            };

            let counter = title_to_counter
                .entry(content.clone())
                .and_modify(|c| *c += 1)
                .or_insert(0);
            let slug_target = if *counter > 0 {
                Cow::Owned(format!("{}-{}", &content, counter))
            } else {
                Cow::Borrowed(&content)
            };
            let slug = html_slug(&slug_target);
            titles.push(Title {
                text: content,
                depth,
                start_md_offset: heading
                    .position
                    .as_ref()
                    .ok_or(anyhow!("heading has no position"))?
                    .start
                    .offset,
                slug,
            });
        }
        Node::Paragraph(paragraph) => {
            for child in &paragraph.children {
                md_collect_titles(child, title_to_counter, titles)?;
            }
        }
        _ => {}
    }
    Ok(())
}

fn md_parse_metadata(md_content: &str) -> anyhow::Result<(&str, Vec<&str>)> {
    let title_line = md_content
        .lines()
        .next()
        .ok_or(anyhow!("missing title line in article metadata"))?;
    let title = title_line
        .strip_prefix("Title: ")
        .ok_or(anyhow!(
            "missing `Title: ` in title line in article metadata"
        ))?
        .trim_ascii();

    let tags_line = md_content
        .lines()
        .nth(1)
        .ok_or(anyhow!("missing tags line in article metadata"))?;
    let tags_str = tags_line
        .strip_prefix("Tags: ")
        .ok_or(anyhow!("missing `Tags: ` in tags line in article metadata"))?;
    let tags: Vec<&str> = tags_str.split(", ").map(|s| s.trim_ascii()).collect();

    Ok((title, tags))
}

fn html_slug(s: &str) -> String {
    let mut res = String::with_capacity(s.len());

    for c in s.chars() {
        match c {
            '+' => res.push_str("plus"),
            '#' => res.push_str("sharp"),
            c if c.is_ascii_alphanumeric() => res.push(c.to_ascii_lowercase()),
            // Other runes are mapped to `-`, but we avoid consecutive `-`.
            _ if !res.ends_with('-') => {
                res.push('-');
            }
            _ => {}
        };
    }
    res.trim_matches(|c| c == '-').to_owned()
}

fn md_to_html(md_content: &str) -> anyhow::Result<Vec<u8>> {
    let mut sb = Vec::with_capacity(md_content.len());
    let md_ast = markdown::to_mdast(
        md_content,
        &ParseOptions {
            constructs: markdown::Constructs {
                gfm_autolink_literal: false,
                ..markdown::Constructs::gfm()
            },
            gfm_strikethrough_single_tilde: true,
            ..Default::default()
        },
    )
    .map_err(|err| anyhow!("failed to parse markdown: {:?}", err))?;

    let mut footnote_defs = Vec::new();

    if let Node::Root(r) = &md_ast
        && r.children.len() == 1
        && let Node::Paragraph(p) = &r.children[0]
    {
        for c in &p.children {
            md_to_html_rec(&mut sb, &mut footnote_defs, c, &[], false)?;
        }
        return Ok(sb);
    }
    md_to_html_rec(&mut sb, &mut footnote_defs, &md_ast, &[], false)?;

    Ok(sb)
}

fn md_html_append(md_content: &str, html_content: &mut Vec<u8>) -> anyhow::Result<()> {
    let mut footnote_defs = Vec::new();

    let md_ast = markdown::to_mdast(
        md_content,
        &ParseOptions {
            constructs: markdown::Constructs {
                gfm_autolink_literal: false,
                ..markdown::Constructs::gfm()
            },
            gfm_strikethrough_single_tilde: true,
            ..Default::default()
        },
    )
    .map_err(|err| anyhow!("failed to parse markdown: {:?}", err))?;

    let mut md_titles = Vec::with_capacity(12);
    let mut title_to_counter: BTreeMap<String, u8> = BTreeMap::new();
    md_collect_titles(&md_ast, &mut title_to_counter, &mut md_titles)?;

    md_to_html_rec(html_content, &mut footnote_defs, &md_ast, &md_titles, false)?;
    md_render_footnote_definitions(html_content, &footnote_defs)?;

    Ok(())
}

fn capitalize_first(s: &str) -> Cow<'_, str> {
    if s.is_empty() {
        return Cow::Borrowed(s);
    }

    let mut chars = s.chars();
    let c = chars.next().unwrap(); // Safe due to `is_empty()` check.
    if c.is_uppercase() {
        Cow::Borrowed(s)
    } else {
        Cow::Owned(c.to_uppercase().collect::<String>() + chars.as_str())
    }
}

fn md_to_html_rec(
    content: &mut Vec<u8>,
    footnote_defs: &mut Vec<FootnoteDefinition>,
    node: &Node,
    titles: &[Title],
    inside_thead: bool,
) -> anyhow::Result<()> {
    match node {
        Node::Root(root) => {
            for child in &root.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
        }
        Node::Blockquote(blockquote) => {
            writeln!(content, "<blockquote>")?;

            for child in &blockquote.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</blockquote>")?;
        }
        Node::FootnoteDefinition(footnote_definition) => {
            footnote_defs.push(footnote_definition.clone());
        }
        Node::MdxJsxFlowElement(_mdx_jsx_flow_element) => todo!(),
        Node::List(list) => {
            let tag = if list.ordered { "ol" } else { "ul" };
            if let Some(start) = list.start
                && start > 1
            {
                writeln!(content, r#"<{} start="{}">"#, tag, start)?;
            } else {
                writeln!(content, r#"<{}>"#, tag)?;
            }
            for child in &list.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</{}>", tag)?;
        }
        Node::MdxjsEsm(_mdxjs_esm) => todo!(),
        Node::Toml(_toml) => todo!(),
        Node::Yaml(_yaml) => todo!(),
        Node::Break(_) => todo!(),
        Node::InlineCode(inline_code) => {
            let sanitized = text_sanitize_for_html(&inline_code.value, false);
            write!(content, "<code>{}</code>", sanitized)?;
        }
        Node::InlineMath(_inline_math) => todo!(),
        Node::Delete(delete) => {
            write!(content, "<del>")?;
            for child in &delete.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            write!(content, "</del>")?;
        }
        Node::Emphasis(emphasis) => {
            write!(content, "<em>")?;
            for child in &emphasis.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            write!(content, "</em>")?;
        }
        Node::MdxTextExpression(_mdx_text_expression) => todo!(),
        Node::FootnoteReference(footnote_reference) => {
            writeln!(
                content,
                r##"<sup class="footnote-ref"><a href="#fn-{}" id="fnref-{}" data-footnote-ref>{}</a></sup>"##,
                &footnote_reference.identifier,
                footnote_reference.identifier,
                footnote_reference
                    .label
                    .as_ref()
                    .ok_or(anyhow!("footnote reference has no label"))?,
            )?;
        }
        Node::Html(html) => {
            writeln!(content, "{}", html.value)?;
        }
        Node::Image(image) => {
            write!(
                content,
                r#"<img src="{}" alt="{}" />"#,
                image.url, image.alt
            )?;
        }
        Node::ImageReference(_image_reference) => todo!(),
        Node::MdxJsxTextElement(_mdx_jsx_text_element) => todo!(),
        Node::Link(link) => {
            write!(
                content,
                r#"<a href="{}">"#,
                text_sanitize_for_html(&link.url, false)
            )?;
            for child in &link.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            write!(content, r#"</a>"#)?;
        }
        Node::LinkReference(_link_reference) => todo!(),
        Node::Strong(strong) => {
            write!(content, "<strong>")?;
            for child in &strong.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            write!(content, "</strong>")?;
        }
        Node::Text(text) => {
            let sanitized = text_sanitize_for_html(&text.value, false);
            write!(content, "{}", sanitized)?;
        }
        Node::Code(code) => {
            let sanitized = text_sanitize_for_html(&code.value, false);
            let lang = code.lang.as_ref().unwrap(); // Safe due to `md_lint_rec` checking that.
            write!(
                content,
                r#"<pre>
  <div class="code-header">
    <span>{}</span>
    <button class="copy-code" type="button"><svg aria-hidden="true" focusable="false" class="octicon octicon-copy" viewBox="0 0 16 16" width="16" height="16" fill="currentColor" display="inline-block" overflow="visible" style="vertical-align: text-bottom;"><path d="M0 6.75C0 5.784.784 5 1.75 5h1.5a.75.75 0 0 1 0 1.5h-1.5a.25.25 0 0 0-.25.25v7.5c0 .138.112.25.25.25h7.5a.25.25 0 0 0 .25-.25v-1.5a.75.75 0 0 1 1.5 0v1.5A1.75 1.75 0 0 1 9.25 16h-7.5A1.75 1.75 0 0 1 0 14.25Z"></path><path d="M5 1.75C5 .784 5.784 0 6.75 0h7.5C15.216 0 16 .784 16 1.75v7.5A1.75 1.75 0 0 1 14.25 11h-7.5A1.75 1.75 0 0 1 5 9.25Zm1.75-.25a.25.25 0 0 0-.25.25v7.5c0 .138.112.25.25.25h7.5a.25.25 0 0 0 .25-.25v-7.5a.25.25 0 0 0-.25-.25Z"></path></svg></button>
  </div>
  <code class="language-{}">"#,
                capitalize_first(lang),
                lang,
            )?;

            for line in sanitized.lines() {
                writeln!(
                    content,
                    r#"<span class="line-number"></span><span class="code-hl">{}</span>"#,
                    line
                )?;
            }
            writeln!(content, "</code></pre>")?;
        }
        Node::Math(_math) => todo!(),
        Node::MdxFlowExpression(_mdx_flow_expression) => todo!(),
        Node::Heading(heading) => {
            assert_eq!(heading.children.len(), 1);
            let title = titles
                .iter()
                .find(|t| t.start_md_offset == heading.position.as_ref().unwrap().start.offset)
                .ok_or(anyhow!("failed to find title"))?;

            writeln!(content, r#"<h{} id="{}">"#, heading.depth, title.slug)?;
            write!(content, r##"  <a class="title" href="#{}">"##, title.slug)?;
            for child in &heading.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</a>")?;
            writeln!(
                content,
                r##"  <a class="hash-anchor" href="#{}" aria-hidden="true" onclick="navigator.clipboard.writeText(this.href);"></a>"##,
                title.slug
            )?;
            writeln!(content, "</h{}>", heading.depth)?;
        }
        Node::Table(table) => {
            writeln!(content, "<table>")?;
            let (thead, tbdody) = table.children.split_first().ok_or(anyhow!("expected table element to have thead and tbody as first two children, but it did not"))?;
            writeln!(content, "<thead>")?;
            md_to_html_rec(content, footnote_defs, thead, titles, true)?;
            writeln!(content, "</thead>")?;

            writeln!(content, "<tbody>")?;
            for child in tbdody {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</tbody>")?;

            writeln!(content, "</table>\n\n")?;
        }
        Node::ThematicBreak(_) => {
            writeln!(content, "<hr />")?;
        }
        Node::TableRow(table_row) => {
            writeln!(content, "<tr>")?;
            for child in &table_row.children {
                md_to_html_rec(content, footnote_defs, child, titles, inside_thead)?;
            }
            writeln!(content, "</tr>")?;
        }
        Node::TableCell(table_cell) => {
            let tag = if inside_thead { "th" } else { "td" };
            write!(content, "<{}>", tag)?;
            for child in &table_cell.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</{}>", tag)?;
        }
        Node::ListItem(list_item) => {
            write!(content, "<li>")?;
            let children = if list_item.children.len() == 1
                && let Node::Paragraph(p) = &list_item.children[0]
            {
                &p.children
            } else {
                &list_item.children
            };
            for child in children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</li>")?;
        }
        Node::Definition(_definition) => todo!(),
        Node::Paragraph(paragraph) => {
            write!(content, "<p>")?;
            for child in &paragraph.children {
                md_to_html_rec(content, footnote_defs, child, titles, false)?;
            }
            writeln!(content, "</p>")?;
        }
    }
    Ok(())
}

fn text_sanitize_for_html(s: &str, sanitize_single_quote: bool) -> Cow<'_, str> {
    if !s.contains(['&', '<', '>', '"', '\'']) {
        Cow::Borrowed(s)
    } else {
        let mut result = String::with_capacity(s.len());
        for c in s.chars() {
            match c {
                '&' => result.push_str("&amp;"),
                '<' => result.push_str("&lt;"),
                '>' => result.push_str("&gt;"),
                '"' => result.push_str("&quot;"),
                '\'' => {
                    if sanitize_single_quote {
                        result.push_str("&#39;")
                    } else {
                        result.push('\'');
                    }
                }
                other => result.push(other),
            }
        }
        Cow::Owned(result)
    }
}

fn md_render_toc(content: &mut Vec<u8>, titles: &[Title]) -> anyhow::Result<()> {
    if titles.is_empty() {
        return Ok(());
    }

    writeln!(
        content,
        r#"  <details class="toc"><summary>Table of contents</summary>
<ul>"#
    )?;

    let mut current_depth = titles[0].depth;

    for (i, title) in titles.iter().enumerate() {
        if title.depth > current_depth {
            for _ in 0..(title.depth - current_depth) {
                writeln!(content, "<ul>")?;
            }
        } else if title.depth < current_depth {
            // Close the current <li>, then close the <ul> levels, then close the parent <li>.
            for _ in 0..(current_depth - title.depth) {
                writeln!(content, "</li>\n</ul>")?;
            }
            writeln!(content, "</li>")?; // Close the <li> of the previous same-level item.
        } else if i > 0 {
            // Same level: just close the previous item.
            writeln!(content, "</li>")?;
        }
        current_depth = title.depth;

        writeln!(
            content,
            r##"
  <li>
    <a href="#{}">{}</a>"##,
            title.slug, &title.text,
        )?;
    }

    // Final cleanup: close all remaining open tags.
    let base_depth = titles[0].depth;
    for _ in 0..=(current_depth - base_depth) {
        writeln!(content, "</li>\n</ul>")?;
    }
    writeln!(content, "</details>\n")?;

    Ok(())
}

fn md_render_article(
    git_stat: GitStat,
    html_header: &[u8],
    html_footer: &[u8],
    cache: &mut HashMap<u64, Article>,
) -> anyhow::Result<Article> {
    let start = Instant::now();
    assert!(!html_header.is_empty());
    assert!(!html_footer.is_empty());

    let md_content_bytes = fs::read(&git_stat.path_from_git_root)
        .with_context(|| format!("failed to read file: {}", &git_stat.path_from_git_root))?;
    let hash = hash_article_inputs(html_header, html_footer, &md_content_bytes);
    if let Some(article) = cache.get(&hash) {
        return Ok(article.clone());
    }
    let md_content_bytes_len = md_content_bytes.len();
    let md_content = String::from_utf8(md_content_bytes).with_context(|| {
        format!(
            "failed to validate utf8: path={}",
            &git_stat.path_from_git_root
        )
    })?;

    let (md_root_title, tags) = md_parse_metadata(&md_content)?;

    let metadata_delim = "---";
    let metadata_delim_pos = md_content.find(metadata_delim).ok_or_else(|| {
        anyhow!(
            "no metadata delimiter found: path={}",
            &git_stat.path_from_git_root
        )
    })?;
    let (_, md_content_article) = md_content.split_at(metadata_delim_pos + metadata_delim.len());

    let md_path = PathBuf::from(&git_stat.path_from_git_root);
    let html_path = md_path.with_extension("html");
    let md_ast = match markdown::to_mdast(
        md_content_article,
        &ParseOptions {
            constructs: markdown::Constructs {
                gfm_autolink_literal: false,
                ..markdown::Constructs::gfm()
            },
            gfm_strikethrough_single_tilde: true,
            ..Default::default()
        },
    ) {
        Ok(x) => x,
        Err(err) => bail!(
            "failed to parse markdown: path={} err={:?}",
            &git_stat.path_from_git_root,
            err
        ),
    };

    md_lint_rec(&md_ast, &md_path)?;

    let mut sb: Vec<u8> = Vec::with_capacity(md_content_bytes_len * 8);
    let html_root_title = String::from_utf8(md_to_html(md_root_title)?).with_context(|| {
        format!(
            "failed to validate utf8 for root title: path={}",
            &git_stat.path_from_git_root
        )
    })?;

    writeln!(
        sb,
        "{}
<!DOCTYPE html>\n<html>\n<head>\n<title>{}</title>",
        AUTOGENERATED_COMMENT, &md_root_title,
    )?;
    sb.extend(html_header);
    writeln!(sb, r#"{}<div class="article-prelude">"#, "\n")?;

    let (creation_date, _time) = git_stat.creation_date.split_once("T").with_context(|| {
        format!(
            "failed to parse creation date: path={} date={}",
            &git_stat.path_from_git_root, &git_stat.creation_date,
        )
    })?;

    let (modification_date, _time) =
        git_stat
            .modification_date
            .split_once("T")
            .with_context(|| {
                format!(
                    "failed to parse modification date: path={} date={}",
                    &git_stat.path_from_git_root, &git_stat.modification_date,
                )
            })?;
    writeln!(
        sb,
        r#"{}

<p class="publication-date">Published on {}. Last modified on {}.</p>"#,
        BACK_LINK, creation_date, modification_date,
    )?;
    writeln!(sb, r#"</div>"#)?;
    writeln!(
        sb,
        r#"<div class="article-title">
  <h1>{}</h1>"#,
        &html_root_title,
    )?;

    write!(sb, r#"<div class="tags"> "#)?;
    for tag in &tags {
        let id = html_slug(tag);
        write!(
            sb,
            r#"<a href="/blog/articles-by-tag.html#{}" class="tag">{}</a> "#,
            id, tag
        )?;
    }
    writeln!(sb, r#"</div>"#)?;
    writeln!(sb, r#"</div>"#)?;

    let mut md_titles = Vec::with_capacity(12);
    let mut title_to_counter: BTreeMap<String, u8> = BTreeMap::new();
    md_collect_titles(&md_ast, &mut title_to_counter, &mut md_titles)?;

    md_render_toc(&mut sb, &md_titles)?;

    let mut footnote_defs = Vec::with_capacity(8);
    md_to_html_rec(&mut sb, &mut footnote_defs, &md_ast, &md_titles, false)?;
    md_render_footnote_definitions(&mut sb, &footnote_defs)?;

    writeln!(sb, "{}", BACK_LINK)?;
    sb.extend(html_footer);

    let article = Article {
        git_stat,
        html_title: html_root_title,
        html_path,
        tags: tags.iter().map(|t| t.to_string()).collect(),
        html_output: sb,
    };

    cache.insert(hash, article.clone());

    println!(
        "⏳ cache miss: generated {} in {} us",
        &article.git_stat.path_from_git_root,
        Instant::now().duration_since(start).as_micros()
    );

    Ok(article)
}

fn md_render_footnote_definitions(
    content: &mut Vec<u8>,
    footnote_defs: &[FootnoteDefinition],
) -> anyhow::Result<()> {
    if footnote_defs.is_empty() {
        return Ok(());
    }

    writeln!(
        content,
        r#"<section class="footnotes" data-footnotes>
<ol>"#
    )?;

    for def in footnote_defs {
        writeln!(content, r#"<li id="fn-{}">"#, def.identifier)?;

        assert_eq!(1, def.children.len());
        let child = &def.children[0];
        let children = match child {
            Node::Paragraph(p) => &p.children,
            other => panic!("unexpected footnote definition: {:#?}", other),
        };
        writeln!(content, r#"<p>"#)?;
        for child in children {
            md_to_html_rec(content, &mut vec![], child, &[], false)?;
        }

        writeln!(
            content,
            r##"<a href="#fnref-{}" class="footnote-backref" data-footnote-backref data-footnote-backref-idx="{}" aria-label="Back to reference {}">↩</a>"##,
            &def.identifier, &def.identifier, &def.identifier
        )?;

        writeln!(
            content,
            r#"</p>
</li>"#
        )?;
    }
    writeln!(
        content,
        r#"</ol>
</section>"#
    )?;
    Ok(())
}

fn generate_tags_page(
    articles: &[Article],
    html_header: &[u8],
    html_footer: &[u8],
) -> anyhow::Result<()> {
    let start = Instant::now();

    let mut tag_to_articles = BTreeMap::new();

    for article in articles {
        for tag in &article.tags {
            tag_to_articles
                .entry(tag.clone())
                .or_insert_with(Vec::new)
                .push(article);
        }
    }

    let mut sb: Vec<u8> = Vec::with_capacity(64000);
    writeln!(
        sb,
        "<!DOCTYPE html>\n<html>\n<head>\n<title>Articles by tag</title>",
    )?;
    sb.extend(html_header);
    sb.extend(BACK_LINK.as_bytes());

    writeln!(sb, "\n<h1>Articles by tag</h1>\n<ul>",)?;

    for (tag, articles) in &mut tag_to_articles {
        articles.sort_by(|a, b| a.git_stat.creation_date.cmp(&b.git_stat.creation_date));

        writeln!(
            sb,
            "<li id=\"{}\"><span class=\"tag\">{}</span><ul>",
            html_slug(tag),
            tag
        )?;

        for a in articles {
            sb.extend(b"<li>\n");
            let (date, _time) = a
                .git_stat
                .creation_date
                .split_once("T")
                .ok_or(anyhow!("failed to parse date"))?;
            writeln!(sb, "  <span class=\"date\">{}</span>", date)?;
            writeln!(
                sb,
                "  <a href=\"{}\">{}</a>\n</li>",
                &a.html_path.to_string_lossy(),
                &a.html_title
            )?;
        }
        writeln!(sb, "</ul></li>",)?;
    }
    writeln!(sb, "</ul>",)?;

    sb.extend(html_footer);
    fs::write("articles-by-tag.html", sb).context("failed to write articles-by-tag.html")?;

    println!(
        "⚙️ generated articles-by-tag.html: {} us",
        Instant::now().duration_since(start).as_micros()
    );
    Ok(())
}

fn generate_article_rss(
    sb: &mut Vec<u8>,
    base_uuid: &uuid::Uuid,
    article: &Article,
) -> anyhow::Result<()> {
    let article_uuid =
        uuid::Uuid::new_v5(base_uuid, article.html_path.to_string_lossy().as_bytes());
    write!(
        sb,
        r#"
<entry>
<title>{}</title>
<link href="{}/{}"/>
<id>urn:uuid:{}</id>
<updated>{}</updated>
<published>{}</published>
</entry>
    "#,
        text_sanitize_for_html(&article.html_title, true),
        BASE_URL,
        article
            .html_path
            .to_str()
            .ok_or(anyhow!("failed to convert path to str"))?,
        &article_uuid,
        article.git_stat.modification_date,
        article.git_stat.creation_date,
    )?;

    Ok(())
}

fn generate_rss(articles: &mut [Article]) -> anyhow::Result<()> {
    let start = Instant::now();

    articles.sort_by(|a, b| {
        a.git_stat
            .creation_date
            .as_str()
            .cmp(&b.git_stat.creation_date)
    });

    let mut sb = Vec::with_capacity(32000);
    let blog_uuid = uuid::Builder::from_bytes(FEED_UUID).into_uuid();

    let last_modification_date = articles
        .last()
        .as_ref()
        .ok_or(anyhow!("no last article"))?
        .git_stat
        .modification_date
        .as_str();
    writeln!(
        sb,
        r#"<?xml version="1.0" encoding="utf-8"?>
<feed xmlns="http://www.w3.org/2005/Atom">
<title>Philippe Gaultier's blog</title>
<link href="{}"/>
<updated>{}</updated>
<author>
  <name>Philippe Gaultier</name>
</author>
<id>urn:uuid:{}</id>
"#,
        BASE_URL, last_modification_date, blog_uuid,
    )?;

    for a in articles {
        generate_article_rss(&mut sb, &blog_uuid, a)?;
    }

    sb.extend(b"</feed>");
    fs::write("feed.xml", sb).context("failed to write feed.xml")?;

    println!(
        "⚙️ generated feed.xml: {} us",
        Instant::now().duration_since(start).as_micros()
    );

    Ok(())
}

